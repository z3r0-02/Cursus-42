char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	// if (!s)
	// 	return ((void *)0);
	while (c > 127)
	{
		c = c % 128;
	}
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)(s + i));
		i++;
	}
	if (s[i] == c)
		return ((char *)(s + i));
	return (0);
}
